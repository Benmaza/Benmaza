"""
Operadores
"""

# Operadores aritméticos
print(f"Suma 2+3={2+3}")
print(f"Resta 2-3={2-3}")
print(f"Multiplicación 2*3={2*3}")
print(f"División 2/3={2/3}")
print(f"División entera 2//3={2//3}")
print(f"Resto de la división 2%3={2%3}")
print(f"Potencia 2**3={2**3}")

# Operadores de comparación
print(f"Igualdad 2==3={2==3}")
print(f"Diferencia 2!=3={2!=3}")
print(f"Mayor que 2>3={2>3}")
print(f"Menor que 2<3={2<3}")
print(f"Mayor o igual que 2>=3={2>=3}")

# Operadores lógicos
print(f"Y lógico True and False={True and False}")
print(f"O lógico True or False={True or False}")
print(f"AND && : 10>5 and 5>3={10>5 and 5>3}")
print(f"OR || : 10>5 or 5>3={10>5 or 5>3}")
print(f"NOT ! : not 10>5={not 10>5}")